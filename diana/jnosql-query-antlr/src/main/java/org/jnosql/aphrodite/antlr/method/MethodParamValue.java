package org.jnosql.aphrodite.antlr.method;

import org.jnosql.query.ParamValue;

import java.util.Objects;

final class MethodParamValue implements ParamValue {

    private final String value;

    MethodParamValue(String value) {
        this.value = value.concat("_")
                .concat(Long.toString(System.nanoTime()));
    }

    @Override
    public String get() {
        return value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (!(o instanceof MethodParamValue)) {
            return false;
        }
        MethodParamValue that = (MethodParamValue) o;
        return Objects.equals(value, that.value);
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(value);
    }

    @Override
    public String toString() {
        return "@" + value;
    }
}
